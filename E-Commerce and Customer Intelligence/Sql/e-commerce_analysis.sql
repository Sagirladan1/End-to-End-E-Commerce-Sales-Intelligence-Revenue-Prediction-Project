/*
Create a monthly revenue summary view using cleaned order dates
Helps track revenue trends by year and month for business reporting
*/
CREATE VIEW monthly_rev_summary AS
SELECT 
YEAR(clean_order_date) AS Year,
MONTH(clean_order_date) AS Month_Number,
MONTHNAME(clean_order_date) AS Month_Name,
SUM(Clean_Revenue) AS Revenue

FROM ecommerce_cleaned

WHERE clean_order_date IS NOT NULL

GROUP BY 
YEAR(clean_order_date),
MONTH(clean_order_date),
MONTHNAME(clean_order_date)

ORDER BY 
Year,
Month_Number;

SELECT * 
FROM monthly_rev_summary;

